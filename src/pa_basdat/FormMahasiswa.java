    public ArrayList<Mahasiswa> listMahasiswa() {
        ArrayList<Mahasiswa> listMahasiswa = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=Perpustakaan;user=sa;password=admin";
            Connection con = DriverManager.getConnection(url);
            String query = "Select * from Mahasiswa";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            Mahasiswa mahasiswa;
            while (rs.next()) {
                mahasiswa = new Mahasiswa(rs.getLong("nim"), rs.getString("nama"), rs.getString("fakultas"), rs.getString("prodi"), rs.getLong("id_petugas"));
                listMahasiswa.add(mahasiswa);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return listMahasiswa;
    }
    
    public void showMahasiswa() {
        ArrayList<Mahasiswa> list = listMahasiswa();
        DefaultTableModel model = (DefaultTableModel)tabelMahasiswa.getModel();
        Object[] row = new Object[model.getColumnCount()];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getNim();
            row[1] = list.get(i).getNama();
            row[2] = list.get(i).getFakultas();
            row[3] = list.get(i).getProdi();
            row[4] = list.get(i).getIdPetugas();
            model.addRow(row);
        }
    }

