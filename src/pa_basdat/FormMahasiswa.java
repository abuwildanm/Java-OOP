    private void tabelMahasiswaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswaMouseClicked
        int row = tabelMahasiswa.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tabelMahasiswa.getModel();
        nim.setText(model.getValueAt(row, 0).toString());
        nama.setText(model.getValueAt(row, 1).toString());
        fakultas.setText(model.getValueAt(row, 2).toString());
        String prodi = model.getValueAt(row, 3).toString();
        switch (prodi) {
            case "Teknik Informartika" : this.prodi.setSelectedIndex(0); break;
            case "Teknik Komputer" : this.prodi.setSelectedIndex(1); break;
            case "Sistem Informasi" : this.prodi.setSelectedIndex(2); break;
            case "Pendidikan Teknologi Informasi" : this.prodi.setSelectedIndex(3); break;
            case "Teknologi Informasi" : this.prodi.setSelectedIndex(4); break;
        }
        idPetugas.setText(model.getValueAt(row, 4).toString());
    }//GEN-LAST:event_tabelMahasiswaMouseClicked

