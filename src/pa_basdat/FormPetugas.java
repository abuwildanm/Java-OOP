    public ArrayList<Petugas> listPetugas() {
        ArrayList<Petugas> listPetugas = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=Perpustakaan;user=sa;password=admin";
            Connection con = DriverManager.getConnection(url);
            String query = "Select * from Petugas";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            Petugas petugas;
            while (rs.next()) {
                petugas = new Petugas(rs.getLong("id_petugas"), rs.getString("nama_petugas"), rs.getString("jabatan"), rs.getInt("jam_kerja"), rs.getInt("gaji"));
                listPetugas.add(petugas);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return listPetugas;
    }
    
    public void showPetugas() {
        ArrayList<Petugas> list = listPetugas();
        DefaultTableModel model = (DefaultTableModel)tabelPetugas.getModel();
        Object[] row = new Object[model.getColumnCount()];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getNama();
            row[2] = list.get(i).getJabatan();
            row[3] = list.get(i).getJamKerja();
            row[4] = list.get(i).getGaji();
            model.addRow(row);
        }
    }
    
