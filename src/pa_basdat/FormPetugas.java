    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=Perpustakaan;user=sa;password=admin";
            Connection con = DriverManager.getConnection(url);
            String query = "Insert into Petugas values (?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, id.getText());
            pst.setString(2, nama.getText());
            pst.setString(3, jabatan.getSelectedItem().toString());
            pst.setString(4, jamKerja.getSelectedItem().toString());
            pst.setString(5, gaji.getText());
            pst.executeUpdate();
            
            DefaultTableModel model = (DefaultTableModel)tabelPetugas.getModel();
            model.setRowCount(0);
            this.showPetugas();
            JOptionPane.showMessageDialog(null, "Inserted Successfully!");
            con.close();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

