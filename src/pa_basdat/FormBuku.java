    private void tabelBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelBukuMouseClicked
        int row = tabelBuku.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)tabelBuku.getModel();
        nomor.setText(model.getValueAt(row, 0).toString());
        String kategori = model.getValueAt(row, 1).toString();
        switch (kategori) {
            case "Statistic" : this.kategori.setSelectedIndex(0); break;
            case "Pemrograman" : this.kategori.setSelectedIndex(1); break;
            case "Multimedia" : this.kategori.setSelectedIndex(2); break;
            case "Database" : this.kategori.setSelectedIndex(3); break;
            case "Sistem Operasi" : this.kategori.setSelectedIndex(4); break;
        }
        judul.setText(model.getValueAt(row, 2).toString());
        penulis.setText(model.getValueAt(row, 3).toString());
        penerbit.setText(model.getValueAt(row, 4).toString());
        tanggal.setText(model.getValueAt(row, 5).toString());
        rak.setText(model.getValueAt(row, 6).toString());
    }//GEN-LAST:event_tabelBukuMouseClicked

