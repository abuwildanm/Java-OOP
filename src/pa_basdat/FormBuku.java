    public ArrayList<Buku> listBuku() {
        ArrayList<Buku> listBuku = new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=Perpustakaan;user=sa;password=admin";
            Connection con = DriverManager.getConnection(url);
            String query = "Select * from Buku";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            Buku buku;
            while (rs.next()) {
                buku = new Buku(rs.getInt("id_buku"), rs.getString("kategori"), rs.getString("judul_buku"), rs.getString("penulis"), rs.getString("penerbit"), rs.getDate("tahun_terbit"), rs.getInt("id_rak"));
                listBuku.add(buku);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return listBuku;
    }
    
    public void showBuku() {
        ArrayList<Buku> list = listBuku();
        DefaultTableModel model = (DefaultTableModel)tabelBuku.getModel();
        Object[] row = new Object[model.getColumnCount()];
        for (int i = 0; i < list.size(); i++) {
            row[0] = list.get(i).getIdBuku();
            row[1] = list.get(i).getKategori();
            row[2] = list.get(i).getJudul();
            row[3] = list.get(i).getPenulis();
            row[4] = list.get(i).getPenerbit();
            row[5] = list.get(i).getTahun();
            row[6] = list.get(i).getIdRak();
            model.addRow(row);
        }
    }

